package org.xodium.vanillaplus.enums

/**
 * Represents the difficulty levels of a quest.
 * This enumeration is used to categorize quests based on their difficulty.
 * It includes three levels of difficulty:
 * - EASY: Represents a quest with low difficulty, designed for beginners or those seeking a simple challenge.
 * - NORMAL: Represents a quest with moderate difficulty, offering a balanced challenge for most players.
 * - HARD: Represents a challenging quest, suited for experienced players seeking a tough experience.
 */
enum class QuestDifficultyEnum {
    EASY, MEDIUM, HARD,
}